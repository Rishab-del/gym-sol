export const EXERCISE_CATEGORIES = [
  "chest",
  "back",
  "shoulders",
  "biceps",
  "triceps",
  "legs",
  "glutes",
  "core",
  "cardio",
  "forearm",
  "full",
  "mobility",
] as const;

export function getExercisesByCategory(category: string) {
  return EXERCISE_LIBRARY.filter((item) => item.category === category);
}

export function getExercisesByMuscle(muscle: string) {
  return EXERCISE_LIBRARY.filter((item) =>
    item.muscle.toLowerCase().includes(muscle.toLowerCase()),
  );
}

export function searchExercises(query: string) {
  const q = query.trim().toLowerCase();

  if (!q) return EXERCISE_LIBRARY;

  return EXERCISE_LIBRARY.filter((item) => {
    return (
      item.name.toLowerCase().includes(q) ||
      item.muscle.toLowerCase().includes(q) ||
      item.secondaryMuscles.some((m) => m.toLowerCase().includes(q)) ||
      item.equipment.toLowerCase().includes(q) ||
      item.category.toLowerCase().includes(q)
    );
  });
}
